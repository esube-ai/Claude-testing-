export function getCityById(id: string): CityRegulation | undefined {
  return CITIES.find(c => c.id === id);
}

export function detectCityFromGeocode(
  cityName: string,
  stateName: string,
  countyName?: string
): CityRegulation | undefined {
  const cityLower = cityName.toLowerCase().trim();
  const stateLower = stateName.toLowerCase().trim();
  const countyLower = countyName?.toLowerCase().trim() ?? '';

  return CITIES.find(city => {
    const stateMatch =
      city.state.toLowerCase() === stateLower ||
      city.state.toLowerCase() === stateLower.slice(0, 2);

    if (!stateMatch) return false;

    const aliasMatch = city.aliases.some(alias =>
      cityLower.includes(alias) || alias.includes(cityLower)
    );

    const countyMatch = countyLower
      ? city.counties.some(c => countyLower.includes(c) || c.includes(countyLower))
      : false;

    return aliasMatch || countyMatch;
  });
}
